int	ft_str_is_numeric(char *str)
{
	int	i;
	int	j;

	i = 0;
	j = 1;
	while (str[i] != '\0')
	{
		if (!('0' <= str[i] && str[i] <= '9'))
		{
			j = 0;
			break ;
		}
		i++;
	}
	return (j);
}
