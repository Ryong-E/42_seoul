int	ft_str_is_uppercase(char *str)
{
	int	i;
	int	j;

	i = 0;
	j = 1;
	while (str[i] != '\0')
	{
		if (!(('A' <= str[i]) && (str[i] <= 'Z')))
		{
			j = 0 ;
			break ;
		}
		i++;
	}
	return (j);
}
