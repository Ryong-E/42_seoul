void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	num;

	i = 0;
	j = 0;
	while (i < size - 1)
	{
		j = i + 1;
		while (j < size)
		{
			if (tab[i] > tab[j])
			{
				num = tab[i];
				tab[i] = tab[j];
				tab[j] = num;
			}
			j++;
		}
		i++;
	}
}
